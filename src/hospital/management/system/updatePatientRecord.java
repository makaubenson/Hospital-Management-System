    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String patientID = txtPatientID.getText();
        String name = txtName.getText();
        String contactNumber = txtContactNumber.getText();
        String age = txtAge.getText();
        String gender = txtGender.getText();
        String bloodGroup = txtBloodGroup.getText();
        String address = txtAddress.getText();
        String anyMajorDisease= txtAnyMajorDisease.getText();
        
        try{
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
        st.executeUpdate("update patient set name='"+name+"',contactNumber='"+contactNumber+"',age='"+age+"',"
                + "gender='"+gender+"',bloodGroup='"+bloodGroup+"',address='"+address+"',"
                        + "anyMajorDisease='"+anyMajorDisease+"' WHERE patientID='"+patientID+"'  ");
        JOptionPane.showMessageDialog(null, "Successfully Updated!!");
        setVisible(false);
        new updatePatientRecord().setVisible(true);
                }
        //end of try
        catch(Exception e){
        JOptionPane.showMessageDialog(null, "Enter data in the correct formart!!");
        
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

