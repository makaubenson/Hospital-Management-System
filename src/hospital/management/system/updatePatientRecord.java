    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String patientID = txtPatientID.getText();
        
        try{
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("select * from patient where patientID='"+patientID+"'  ");
        if(rs.next()){
        txtName.setText(rs.getString(2));
        txtContactNumber.setText(rs.getString(3));
        txtAge.setText(rs.getString(4));
        txtGender.setText(rs.getString(5));
        txtBloodGroup.setText(rs.getString(6));
        txtAddress.setText(rs.getString(7));
        txtAnyMajorDisease.setText(rs.getString(8));
        
        txtPatientID.setEditable(false);
        }else{
        JOptionPane.showMessageDialog(null, "patientID does not exist");
        
        
        }
        
        
        }
        catch(Exception e)
        {}
    }//GEN-LAST:event_btnSearchActionPerformed

